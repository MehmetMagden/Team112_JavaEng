package day13_NestedLoops_MethodCreation;

public class C02_NestedLoops {


    /*

     *
     * *
     * * *
     * * * *

     */

    public static void main(String[] args) {

        for (int j = 1; j <= 4; j++) {

            for (int i = 1; i <= j; i++) {
                System.out.print("* ");
            }
            System.out.println();
        }




    }


}
